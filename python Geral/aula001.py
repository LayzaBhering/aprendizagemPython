#Váriaveis + fstring

#A váriavel é uma caixa que guarda um valor.
#Esse valor pode ser resgatado e manipulado.

nome = 'Python'

fraseNome = f'Seu nome é... {nome}'

#A váriavel nome recebe o valor dentro das aspas.
#Ponto importante, lê-se "=" como recebe...
#fraseNome (váriavel) recebe uma frase + a váriavel nome dentro de aspas. Observe que há um f antes da abertura da aspa.
#Essa sintaxe possibilita que o programador manipule váriaveis dentro de strings.
#Caso não tivesse essa fstring + { }, o nome seria apenas um texto dentro de fraseNome. Testa aí!

print(fraseNome)

#print é uma função de impressão. Pode ser útil para entender problemas de desenvolvimento e até a interagir com clientes.